% example_07_01a.m
% 例 7.1：ナイキストの安定判別法（図 7.4）

close all
clear
format compact

disp(' ')
disp('==================================================')
disp(' 例 7.1 ')
disp('==================================================')

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp(' P(s) = 1/(s - 1) ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

s = tf('s');
sysP = 1/(s - 1)

% ------------------------------------------------------
figure(1)

for kP = [0.5 1 2]
    disp(' ')
    disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
    disp(' C(s) = kP ')
    disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

    sysC = kP
    
    disp(' ')
    disp('--------------------------------------------------')
    disp(' =====> L(s) = P(s)C(s)')
    disp('--------------------------------------------------')
    
    sysL = sysP*sysC

    nyquist(sysL)
    hold on
end

figure(1)
hold off

legend('kP = 0.5','kP = 1','kP = 2')
