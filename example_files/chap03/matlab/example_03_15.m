% example_03_15.m
% 例 3.15：単位ステップ応答の定常値の計算
% 【Symbolic Math Toolbox が必要】

disp(' ')
disp('==================================================')
disp(' 例 3.15 ')
disp('==================================================')

clear
format compact

syms s
syms t real

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ P(s) = 10/(s^2 + 2s + 10)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

Ps = 10/(s^2 + 2*s + 10)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ u(t) = 1')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ u(s) = L[u(t)]')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

ut = sym(1)         % もしくは以下のように記述
us = laplace(ut)    % us = 1/s

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ y(s)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

ys = Ps*us
ys = partfrac(ys)   % 省略しても良い


disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ 単位ステップ応答 y(t)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

yt = ilaplace(ys)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ y(t) の極限値')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

yinf = limit(yt,t,Inf)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ 最終値の定理 yinf = P(0)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

yinf = subs(Ps,s,0)

