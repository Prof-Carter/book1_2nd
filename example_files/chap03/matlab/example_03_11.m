% example_03_11.m
% 例 3.11：例 3.9 の単位ステップ応答における部分分数分解
% 　　　　（ヘビサイドの公式）
% 【Symbolic Math Toolbox が必要】

disp(' ')
disp('==================================================')
disp(' 例 3.11 ')
disp('==================================================')

close all
clear
format compact

syms s
syms t real

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ P(s) = 1/(s + 1)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

Ps = 1/(s + 1)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ u(t) = 1')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ u(s) = L[u(t)]')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

ut = sym(1)         % もしくは以下のように記述
us = laplace(ut)    % us = 1/s

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ y(s)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

ys = Ps*us

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ y(s) = k(1)/(s - p(1)) + k(2)/(s - p(2))')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

[num_ys den_ys] = numden(ys);
p = solve(den_ys);      % y(s) の分母を 0 とする解 p
p = sort(p,'descend')   % p を降べきの順に並び替え

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ ヘビサイドの公式')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

k(1,1) = subs((s - p(1))*ys,s,p(1));
k(2,1) = subs((s - p(2))*ys,s,p(2));

k

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+ 単位ステップ応答 y(t)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

yt = k(1)*exp(p(1)*t) + k(2)*exp(p(2)*t)

% ----- グラフの描画 ----------------------------------
figure(1)
fplot(yt,[0 5])
xlabel('t [s]')
ylabel('y(t)')
grid on
