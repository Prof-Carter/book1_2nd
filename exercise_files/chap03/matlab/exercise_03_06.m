% exercise_03_06.m
% 問題 3.6：安定なシステムの単位ステップ応答の定常値
% 【Symbolic Math Toolbox が必要】

clear
format compact

syms s
syms t real

disp(' ')
disp('==================================================')
disp(' 問題 3.6 (1) ')
disp('==================================================')

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' P(s) = (s + 1)/(s + 2)')
disp('++++++++++++++++++++++++++++++++++++++++++')

Ps = (s + 1)/(s + 2)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' u(t) = 1')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' u(s) = L[u(t)]')
disp('++++++++++++++++++++++++++++++++++++++++++')

ut = sym(1)         % もしくは以下のように記述
us = laplace(ut)    % us = 1/s

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' y(s)')
disp('++++++++++++++++++++++++++++++++++++++++++')

ys = Ps*us

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' y(s) を部分分数分解')
disp('++++++++++++++++++++++++++++++++++++++++++')

ys = partfrac(ys)   % 省略しても良い

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' 単位ステップ応答 y(t)')
disp('++++++++++++++++++++++++++++++++++++++++++')

yt = ilaplace(ys)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' y(t) の極限値')
disp('++++++++++++++++++++++++++++++++++++++++++')

yinf = limit(yt,t,Inf)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++')
disp(' 最終値の定理 yinf = P(0)')
disp('++++++++++++++++++++++++++++++++++++++++++')

yinf = subs(Ps,s,0)


