% exercise_02_10.m
% 問題 2.10
% 伝達関数の極と零点

clear
format compact

disp(' ')
disp('==================================================')
disp(' 問題 2.10')
disp('==================================================')

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+  伝達関数 P(s)')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

s = tf('s');                        % ラプラス演算子 s
sysN = s^2 + 2*s + 3;               % 分子 N(s)
sysD = 4*s^4 + 5*s^3 + s^2 + s + 5; % 分母 D(s)
sysP = sysN/sysD                    % 伝達関数 P(s) = N(s)/D(s)

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+  伝達関数 P(s) の極 p')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

pole(sysP)                          % 伝達関数 P(s) の極

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+  伝達関数 P(s) の零点 z')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

zero(sysP)                          % 伝達関数 P(s) の零点

disp(' ')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')
disp('+  伝達関数 P(s) の零点 z，極 p，ゲイン K')
disp('++++++++++++++++++++++++++++++++++++++++++++++++++')

[z p K] = zpkdata(sysP,'v')         % 伝達関数 P(s) の零点，極，ゲイン
